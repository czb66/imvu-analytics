# ==================== 报告生成核心逻辑 ====================

def _generate_report_for_user(
    user: User,
    products: List[Dict],
    report_type: str  # 'daily' or 'weekly'
) -> bool:
    """
    为单个用户生成并发送报告
    Returns: 是否发送成功
    """
    lang = _get_user_language(user)
    tpl = REPORT_TEMPLATES.get(lang, REPORT_TEMPLATES['en'])
    
    today = date.today()
    date_str = today.strftime('%Y-%m-%d')
    
    if report_type == 'daily':
        subject = tpl.get('daily_subject', app_name=config.APP_NAME, date=date_str)
    else:
        # 每周报告显示本周范围
        week_start = today - timedelta(days=today.weekday())
        week_end = week_start + timedelta(days=6)
        date_str = f"{week_start.strftime('%m/%d')} - {week_end.strftime('%m/%d')}"
        subject = tpl.get('weekly_subject', app_name=config.APP_NAME, date=date_str)
    
    if not products:
        logger.info(f"用户 {user.email} 没有产品数据，跳过{report_type}报告")
        return False
    
    # 分析数据
    analysis = _analyze_with_analytics(products)
    
    # 构建HTML
    top_limit = 5 if report_type == 'daily' else 10
    html_content = _build_report_html(
        analysis=analysis,
        tpl=tpl,
        period=report_type,
        date_str=date_str,
        username=user.username or user.email.split('@')[0],
        top_limit=top_limit
    )
    
    # 发送邮件
    success, msg = email_service.send_report(
        to_emails=[user.email],
        subject=subject,
        html_content=html_content
    )
    
    if success:
        logger.info(f"{report_type.capitalize()}报告已发送给 {user.email}: {msg}")
    else:
        logger.warning(f"{report_type.capitalize()}报告发送失败 {user.email}: {msg}")
    
    return success


def generate_daily_report():
    """
    生成每日报告的定时任务
    为所有订阅每日报告的用户生成并发送报告
    """
    logger.info(f"🕐 定时任务执行: 生成每日报告 - {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    
    try:
        with get_db_context() as db:
            # 查找所有订阅每日报告的用户
            users = db.query(User).filter(
                User.report_preference == 'daily',
                User.is_active == True
            ).all()
        
        total = len(users)
        success_count = 0
        fail_count = 0
        
        for user in users:
            try:
                products = _get_products_for_user(user.id)
                if _generate_report_for_user(user, products, 'daily'):
                    success_count += 1
                else:
                    fail_count += 1
            except Exception as user_err:
                logger.error(f"为用户 {user.email} 生成每日报告失败: {user_err}", exc_info=True)
                fail_count += 1
        
        logger.info(f"✅ 每日报告任务完成: 共 {total} 位订阅用户, 成功 {success_count}, 失败 {fail_count}")
        
    except Exception as e:
        logger.error(f"❌ 每日报告任务执行失败: {e}", exc_info=True)
        # 不向上抛出，避免调度器崩溃
        pass


