def _analyze_with_analytics(products: List[Dict]) -> Dict:
    """使用 AnalyticsService 进行数据分析"""
    from app.services.analytics import AnalyticsService
    
    if not products:
        return {
            'summary': {},
            'top_products': [],
            'bottom_products': [],
            'anomalies': [],
            'visibility': {},
            'traffic': {},
        }
    
    analytics = AnalyticsService(products)
    
    summary = analytics.get_summary_metrics()
    top_products = analytics.get_top_products(limit=10, metric='profit')
    bottom_products = analytics.get_bottom_products(limit=5, metric='profit')
    anomalies = analytics.detect_sales_anomalies(threshold=2.0)
    visibility = analytics.get_visibility_analysis()
    
    # 计算各产品总销量
    for p in top_products:
        p['total_sales'] = (
            (p.get('direct_sales') or 0) +
            (p.get('indirect_sales') or 0) +
            (p.get('promoted_sales') or 0)
        )
    
    for p in bottom_products:
        p['total_sales'] = (
            (p.get('direct_sales') or 0) +
            (p.get('indirect_sales') or 0) +
            (p.get('promoted_sales') or 0)
        )
    
    return {
        'summary': summary,
        'top_products': top_products,
        'bottom_products': bottom_products,
        'anomalies': anomalies,
        'visibility': visibility,
        'traffic': analytics.get_traffic_analysis(),
    }


def _generate_ai_insight(analysis: Dict, lang: str = 'en') -> str:
    """基于分析数据生成AI洞察"""
    summary = analysis.get('summary', {})
    top_products = analysis.get('top_products', [])
    anomalies = analysis.get('anomalies', [])
    
    insights = []
    t = lambda k, **kw: _t(lang, k, **kw)
    
    # 基于销量的洞察
    total_sales = summary.get('total_sales', 0)
    if total_sales == 0:
        insights.append(t('no_data'))
        return insights[0]
    
    # Top产品洞察
    if top_products:
        best = top_products[0]
        best_name = (best.get('product_name') or best.get('product_id', 'N/A'))[:30]
        best_sales = best.get('total_sales', 0)
        if best_sales > 0:
            insights.append(f"🏆 **{best_name}** — {t('sales')}: {int(best_sales)}")
    
    # 异常检测洞察
    if anomalies:
        low_count = sum(1 for a in anomalies if a.get('z_score', 0) < 0)
        high_count = len(anomalies) - low_count
        if low_count > 0:
            insights.append(f"⚠️ {low_count} {t('low_sales')} {t('product_id')}")
        if high_count > 0:
            insights.append(f"🚀 {high_count} {t('high_sales')}")
    
    # 可见性洞察
    visibility = analysis.get('visibility', {})
    if visibility:
        visible_pct = 0
        total_p = visibility.get('visible', {}).get('count', 0) + visibility.get('hidden', {}).get('count', 0)
        if total_p > 0:
            visible_pct = visibility.get('visible', {}).get('count', 0) / total_p * 100
            if visible_pct < 30:
                insights.append(f"💡 {int(visible_pct)}% products visible — consider increasing visibility for hidden items")
    
    # 利润率洞察
    if top_products:
        high_margin = [p for p in top_products if p.get('profit', 0) > 100]
        if len(high_margin) >= 3:
            insights.append(f"💡 {len(high_margin)}+ high-margin products detected — great pricing strategy!")
    
    return '\n'.join(insights) if insights else t('no_data')


