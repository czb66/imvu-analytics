# ==================== 调度器管理 ====================

def start_scheduler():
    """启动定时任务调度器"""
    try:
        if scheduler.running:
            logger.warning("调度器已在运行中，跳过启动")
            return
        
        # 添加每日报告任务 - 每天早上9点(北京时间) = UTC 1:00
        scheduler.add_job(
            func=generate_daily_report,
            trigger=CronTrigger(
                hour=config.REPORT_CRON_HOUR,
                minute=config.REPORT_CRON_MINUTE,
            ),
            id='daily_report',
            name='每日分析报告',
            replace_existing=True,
            max_instances=1,  # 防止并发执行
            misfire_grace_time=3600,  # 错过执行时间1小时内仍可补执行
        )
        
        # 添加每周报告任务 - 每周一早上9点(北京时间)
        scheduler.add_job(
            func=generate_weekly_report,
            trigger=CronTrigger(
                day_of_week='mon',
                hour=config.REPORT_CRON_HOUR,
                minute=config.REPORT_CRON_MINUTE,
            ),
            id='weekly_report',
            name='每周分析报告',
            replace_existing=True,
            max_instances=1,
            misfire_grace_time=7200,
        )
        
        scheduler.start()
        logger.info(f"✅ 定时任务调度器已启动: 每日报告({config.REPORT_CRON_HOUR}:{config.REPORT_CRON_MINUTE:02d} UTC), 每周报告(周一)")
        
    except Exception as e:
        logger.error(f"❌ 启动定时任务调度器失败: {e}", exc_info=True)
        raise


def stop_scheduler():
    """停止定时任务调度器"""
    if scheduler.running:
        scheduler.shutdown(wait=False)
        logger.info("定时任务调度器已停止")
